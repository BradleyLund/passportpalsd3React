const SelectBoxComponent = ({ selectedCountries, onSelectChange }) => {
  const addSelectBox = () => {
    onSelectChange(selectedCountries.length, "Afghanistan", false);
  };

  const removeSelectBox = (index) => {
    const updatedCountries = selectedCountries.filter((_, i) => i !== index);
    console.log("in remove function", index);
    console.log(updatedCountries);
    onSelectChange(index, updatedCountries, true);
  };

  return (
    <div>
      {selectedCountries.map((country, index) => (
        <div key={index}>
          <select
            value={country}
            onChange={(e) => onSelectChange(index, e.target.value)}
          >
            {countries.map((country, idx) => (
              <option key={idx} value={country}>
                {country}
              </option>
            ))}
          </select>
          <button onClick={() => removeSelectBox(index)}>Remove</button>
        </div>
      ))}
      <button onClick={addSelectBox}>+</button>
      <div className="mt-4 text-sm text-gray-600">
        <h3 className="font-medium text-lg mb-2">Selected Countries:</h3>
        {selectedCountries.map((selection, index) => (
          <p key={index}>
            Selection {index + 1}: {selection || "None"}
          </p>
        ))}
      </div>
    </div>
  );
};

export default SelectBoxComponent;
